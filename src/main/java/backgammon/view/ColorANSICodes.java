/**
 * Utility class for ANSI color codes used to style text output in the terminal.
 * Provides color codes for red, blue, and a reset code to revert text to its default style.
 */
public class ColorANSICodes {
    static final String RED = "\033[31m";
    static final String BLUE = "\033[34m";
    static final String RESET = "\033[0m";

    ColorANSICodes() {}
}
